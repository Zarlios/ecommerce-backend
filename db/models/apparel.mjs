import mongoose from "mongoose";

const apparelSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
});

const Apparel = mongoose.model("Apparel", apparelSchema);

export default Apparel;
