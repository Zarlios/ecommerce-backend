import mongoose from "mongoose";

const collectibleSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
});

const Collectibles = mongoose.model("Collectibles", collectibleSchema);

export default Collectibles;
